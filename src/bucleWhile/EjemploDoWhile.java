package bucleWhile;

public class EjemploDoWhile {

	public static void main(String[] args) {
		
		int contador = 1;
		do {
			System.out.println("Estoy en la vuelta " +contador);
			contador++;
		
		}
		while (contador <= 2); {

			System.out.println("Estoy en la vuelta " + contador);
			contador ++;


			}
		System.out.println("Contador = " + contador);

	}

}
