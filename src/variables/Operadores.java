package variables;

public class Operadores {

	public static void main(String[] args) {
		
		int num1, num2;
		double resultado;
		
		num1=5;
		num2=3;
		resultado=num1+num2;
		System.out.println ("El resutado de la suma es:" + resultado);
		
		resultado=num1-num2;
		System.out.println ("El resutado de la resta es:" + resultado);
		
		resultado=num1*num2;
		System.out.println ("El resutado del producto es:" + resultado);
		
		resultado=num1/num2;
		System.out.println ("El resutado de la division es:" + resultado);
	}


}